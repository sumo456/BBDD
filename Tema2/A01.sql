-- Activitats de consultes simples.
-- Emprant la BD ‘ucm’, escriu les consultes que:
-- 1. Accedeix a la BD.

-- 2. Llista les taules contingudes dins de la BD.

-- 3. Descriu l’estructura de la taula ‘pelicules’.

-- 4. Mostra totes les pel·lícules.

-- 5. Mostra el títol i any de producció de totes les pel·lícules, renombra les 
-- columnes a un nom adient.

-- 6. Mostra el títol, pressupost, recaudació i guany (recaudació – pressupost) 
-- de totes les pel·lícules, renombra les columnes a un nom adient, -- ordena 
-- escendentment el resultat pel guany.

-- 7. Llista el nom de totes les productores de les pel·lícules sense que es 
-- repeteixin, ordena el resultat.

-- 8. Llista el títol i durada de totes les pel·lícules ordenades de forma 
-- descendent per la durada. Els que tinguin la mateixa durada han d’estar 
-- ordenats pel títol.

-- 9. Mostra el títol i recaudació de les 10 primeres pel·lícules.

-- 10. Mostra el títol i recaudació de la pel·lícula 10 fins la 15.

-- 11. Llista de forma concatenada «titol de la pel·lícula es va estrenar fa xx 
-- anys» on xx és l’any actual – l’any de la estrena. Ordena el resultat de 
-- forma ascendent pels anys.

-- 12. Descriu l’estructura de la taula ‘actors’.

-- 13. Llista tot el contingut de la taula actors.

-- 14. Llista totes les nacionalitats dels actors sense que es repeteixin, 
-- ordena el resultat.

-- 15. Fes una consulta on aparegui dels actors 7 al 14 de forma concatenada 
-- «nom actor va néixer el data naixement».
